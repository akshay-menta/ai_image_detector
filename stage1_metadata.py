# stage1_metadata.py
# Stage 1: Metadata and EXIF inspection. Free, local, no HTTP requests.
#
# Detection layers (in order of reliability):
#   1. Software / Creator EXIF tag  ← AI tools (Midjourney, DALL-E, Firefly, SD) self-identify here
#   2. PNG text chunks              ← Stable Diffusion / ComfyUI embed full prompt + params
#   3. XMP / IPTC AI claims         ← Adobe Firefly, DALL-E content credentials
#   4. C2PA manifest bytes          ← OpenAI DALL-E, Adobe, newer tools
#   5. Camera EXIF richness         ← Real cameras leave abundant metadata; AI tools rarely do
#   6. Suspicious field patterns    ← Known AI-only field combinations or missing critical fields

